package com.gmsxo.domains.data;

public class Domain {
  private int ID;
  private String domainName;

  public Domain() {}

  public int getID() {
    return ID;
  }
  public void setID(int iD) {
    ID = iD;
  }

  public String getDomainName() {
    return domainName;
  }

  public void setDomainName(String domainName) {
    this.domainName = domainName;
  }
  
}
